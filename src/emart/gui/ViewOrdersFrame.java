    private void loadOrderDetails() {
    try {
        List<OrderPojo> orderList = OrderDAO.getAllOrders(); // Assuming you have a method to retrieve the last 10 orders
        if (orderList.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No orders found.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) viewOrder.getModel();
        model.setRowCount(0); // Clear existing rows
        for (OrderPojo order : orderList) {
            Object[] rowData = {
                    order.getOrderId(),
                    order.getProductId(),
                    order.getQuantity(),
                    order.getUserId()
            };
            model.addRow(rowData);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "DB error: " + ex.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace(); // or log the exception using a logging framework
    }
}

    
    
    
    
    
    
    
    
    
    
    

